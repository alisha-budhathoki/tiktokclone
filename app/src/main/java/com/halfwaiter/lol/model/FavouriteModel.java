package com.halfwaiter.lol.model;

public class FavouriteModel {
    int  favouriteImg;
    String favLikes;

    public FavouriteModel() {
        this.favouriteImg = favouriteImg;
        this.favLikes = favLikes;
    }

    public int getFavouriteImg() {
        return favouriteImg;
    }

    public void setFavouriteImg(int favouriteImg) {
        this.favouriteImg = favouriteImg;
    }

    public String getFavLikes() {
        return favLikes;
    }

    public void setFavLikes(String favLikes) {
        this.favLikes = favLikes;
    }
}
