package com.halfwaiter.lol.model;

public class MostRecentModel {
int recentVideo;
String noLikes;

    public MostRecentModel() {
        this.recentVideo = recentVideo;
        this.noLikes = noLikes;
    }

    public int getRecentVideo() {
        return recentVideo;
    }

    public void setRecentVideo(int recentVideo) {
        this.recentVideo = recentVideo;
    }

    public String getNoLikes() {
        return noLikes;
    }

    public void setNoLikes(String noLikes) {
        this.noLikes = noLikes;
    }
}
