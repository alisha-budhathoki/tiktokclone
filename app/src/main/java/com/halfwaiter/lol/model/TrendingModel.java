package com.halfwaiter.lol.model;

public class TrendingModel {
    int trendingVideo;
    String noTrendingLikes;

    public TrendingModel() {
        this.trendingVideo = trendingVideo;
        this.noTrendingLikes = noTrendingLikes;
    }

    public int getTrendingVideo() {
        return trendingVideo;
    }

    public void setTrendingVideo(int trendingVideo) {
        this.trendingVideo = trendingVideo;
    }

    public String getNoTrendingLikes() {
        return noTrendingLikes;
    }

    public void setNoTrendingLikes(String noTrendingLikes) {
        this.noTrendingLikes = noTrendingLikes;
    }
}
